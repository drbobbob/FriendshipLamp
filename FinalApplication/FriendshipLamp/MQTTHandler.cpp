MQTTHandlerTask::MQTTHandlerTask()
  : TimedTask(0)
{

}

void MQTTHandlerTask::SetupMQTTHandling()
{
  espClient.setTrustAnchors(&cert);

  client.setServer(mqtt_server, 8883);
  client.setCallback(callback);
}

void MQTTHandlerTask::run(uint32_t now)
{
  if(!client.connected())
  {
    reconnectAttempt();
    if(!client.connected())
    {
      setRunTime(now + 5000);
    }
  }
  else
  {
    client.loop();
  }
}

void MQTTHandlerTask::reconnectAttempt()
{
    Serial.print("Attempting MQTT connection...");
    String clientId = "Lamp-";   // Create a random client ID
    clientId += ESP.getChipId();
    // Attempt to connect
    if (client.connect(clientId.c_str(), mqtt_user, mqtt_password)) {
      Serial.println("connected");

      String mqttTopic(mqtt_topic_prefix);
      mqttTopic += "/#";
      client.subscribe(mqttTopic.c_str());   // subscribe the topics here

    } else {
      Serial.print("failed, rc=");
      Serial.print(client.state());
      Serial.println(" try again in 5 seconds");   // Wait 5 seconds before retrying
    }
}

void MQTTHandlerTask::publishMyColor()
{
  if(client.connected())
  {
    String mqttTopic(mqtt_topic_prefix);
    mqttTopic += "/" + ESP.getChipId();
    client.publish(mqttTopic.c_str(), my_led_color);
  }
}
